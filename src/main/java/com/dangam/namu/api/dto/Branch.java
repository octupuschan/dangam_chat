package com.dangam.namu.api.dto;

public class Branch {

	private String branchName;
	private String branchAddrf;
	private String branchAddrs;
	private String branchTelFront;
	private String branchTelMid;
	private String branchTelLast;
	public String getBranchName() {
		return branchName;
	}
	public void setBranchName(String branchName) {
		this.branchName = branchName;
	}
	public String getBranchAddrf() {
		return branchAddrf;
	}
	public void setBranchAddrf(String branchAddrf) {
		this.branchAddrf = branchAddrf;
	}
	public String getBranchAddrs() {
		return branchAddrs;
	}
	public void setBranchAddrs(String branchAddrs) {
		this.branchAddrs = branchAddrs;
	}
	public String getBranchTelFront() {
		return branchTelFront;
	}
	public void setBranchTelFront(String branchTelFront) {
		this.branchTelFront = branchTelFront;
	}
	public String getBranchTelMid() {
		return branchTelMid;
	}
	public void setBranchTelMid(String branchTelMid) {
		this.branchTelMid = branchTelMid;
	}
	public String getBranchTelLast() {
		return branchTelLast;
	}
	public void setBranchTelLast(String branchTelLast) {
		this.branchTelLast = branchTelLast;
	}
	
	
	
}
